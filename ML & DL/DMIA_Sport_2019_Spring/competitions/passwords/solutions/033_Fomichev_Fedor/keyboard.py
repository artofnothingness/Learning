def keyboard_dist(x, y):
    if x.lower() in keyboard.keys() and y.lower() in keyboard.keys():
        x, y = keyboard[x.lower()], keyboard[y.lower()]
        dist = np.abs(x[0] - y[0]) + np.abs(x[1] - y[1])
    else:
        dist = 5
    return dist